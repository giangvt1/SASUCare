package model;

import java.sql.*;

/**
 *
 * @author TRUNG
 */
public class Application {

    private int id, Did;
    private String name, reason;
    private Date date;

    public Application() {
    }

    public Application(int id, int Did, String name, String reason, Date date) {
        this.id = id;
        this.Did = Did;
        this.name = name;
        this.reason = reason;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDid() {
        return Did;
    }

    public void setDid(int Did) {
        this.Did = Did;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
    
}
